（一）改善教学设施，包括建筑物、仪器设备、图书资料等；</br> 
（二）资助有益于国家建设的研究和开发项目； </br>
（三）奖励在教学、科学研究和高新技术开发等工作中做出贡献的哈尔滨工业大学教师、职工；</br> 
